package pei.registro.vo;

public class CapacitacionVO extends siges.common.vo.Vo{
	private long capInstitucion;
	private int capCodigo;
	private String capNombreFormacion;
	private long capDocentes;
	private int capUniversidad;
	private String capNombreUniversidad;
	
	/**
	 * @return the capInstitucion
	 */
	public long getCapInstitucion() {
		return capInstitucion;
	}
	/**
	 * @param capInstitucion the capInstitucion to set
	 */
	public void setCapInstitucion(long capInstitucion) {
		this.capInstitucion = capInstitucion;
	}
	/**
	 * @return the capNombreFormacion
	 */
	public String getCapNombreFormacion() {
		return capNombreFormacion;
	}
	/**
	 * @param capNombreFormacion the capNombreFormacion to set
	 */
	public void setCapNombreFormacion(String capNombreFormacion) {
		this.capNombreFormacion = capNombreFormacion;
	}
	/**
	 * @return the capDocentes
	 */
	public long getCapDocentes() {
		return capDocentes;
	}
	/**
	 * @param capDocentes the capDocentes to set
	 */
	public void setCapDocentes(long capDocentes) {
		this.capDocentes = capDocentes;
	}
	/**
	 * @return the capUniversidad
	 */
	public int getCapUniversidad() {
		return capUniversidad;
	}
	/**
	 * @param capUniversidad the capUniversidad to set
	 */
	public void setCapUniversidad(int capUniversidad) {
		this.capUniversidad = capUniversidad;
	}
	/**
	 * @return the capCodigo
	 */
	public int getCapCodigo() {
		return capCodigo;
	}
	/**
	 * @param capCodigo the capCodigo to set
	 */
	public void setCapCodigo(int capCodigo) {
		this.capCodigo = capCodigo;
	}
	/**
	 * @return the capNombreUniversidad
	 */
	public String getCapNombreUniversidad() {
		return capNombreUniversidad;
	}
	/**
	 * @param capNombreUniversidad the capNombreUniversidad to set
	 */
	public void setCapNombreUniversidad(String capNombreUniversidad) {
		this.capNombreUniversidad = capNombreUniversidad;
	}
}
