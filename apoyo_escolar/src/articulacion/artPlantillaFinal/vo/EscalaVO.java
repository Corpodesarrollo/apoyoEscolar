public class EscalaVO {
	private String escNombre;
	private float escValorMinimo;
	private float escValorMaximo;
	
	/**
	 * @return Return the escNombre.
	 */
	public String getEscNombre() {
		return escNombre;
	}
	/**
	 * @param escNombre The escNombre to set.
	 */
	public void setEscNombre(String escNombre) {
		this.escNombre = escNombre;
	}
	/**
	 * @return Return the escValorMaximo.
	 */
	public float getEscValorMaximo() {
		return escValorMaximo;
	}
	/**
	 * @param escValorMaximo The escValorMaximo to set.
	 */
	public void setEscValorMaximo(float escValorMaximo) {
		this.escValorMaximo = escValorMaximo;
	}
	/**
	 * @return Return the escValorMinimo.
	 */
	public float getEscValorMinimo() {
		return escValorMinimo;
	}
	/**
	 * @param escValorMinimo The escValorMinimo to set.
	 */
	public void setEscValorMinimo(float escValorMinimo) {
		this.escValorMinimo = escValorMinimo;
	}
}
