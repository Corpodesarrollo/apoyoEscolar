package articulacion.inscripcion.vo;

import siges.common.vo.Vo;

public class InscripcionVO extends Vo{
	
	private int artinsGrupo;
	private int artinsCodest;
	private int artinsCodasgi;
	private String cuposn;
	private String nombreEstudiante;
	
	public int getArtinsCodasgi() {
		return artinsCodasgi;
	}
	public void setArtinsCodasgi(int artinsCodasgi) {
		this.artinsCodasgi = artinsCodasgi;
	}
	public int getArtinsCodest() {
		return artinsCodest;
	}
	public void setArtinsCodest(int artinsCodest) {
		this.artinsCodest = artinsCodest;
	}
	public int getArtinsGrupo() {
		return artinsGrupo;
	}
	public void setArtinsGrupo(int artinsGrupo) {
		this.artinsGrupo = artinsGrupo;
	}
	public String getCuposn() {
		return cuposn;
	}
	public void setCuposn(String cuposn) {
		this.cuposn = cuposn;
	}
	public String getNombreEstudiante() {
		return nombreEstudiante;
	}
	public void setNombreEstudiante(String nombreEstudiante) {
		this.nombreEstudiante = nombreEstudiante;
	}

		
}
	
	
	



