package util;

public class BitacoraDto {
	
	private String usuario;
	private int modulo;
	private int submodulo;
	private int tipoLog;
	private long colegio;
	private int jornada;
	private int sede;
	private String perfil;
	private String descripcion;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public int getModulo() {
		return modulo;
	}
	public void setModulo(int modulo) {
		this.modulo = modulo;
	}
	public int getSubmodulo() {
		return submodulo;
	}
	public void setSubmodulo(int submodulo) {
		this.submodulo = submodulo;
	}
	public int getTipoLog() {
		return tipoLog;
	}
	public void setTipoLog(int tipoLog) {
		this.tipoLog = tipoLog;
	}
	public long getColegio() {
		return colegio;
	}
	public void setColegio(long colegio) {
		this.colegio = colegio;
	}
	public int getJornada() {
		return jornada;
	}
	public void setJornada(int jornada) {
		this.jornada = jornada;
	}
	public int getSede() {
		return sede;
	}
	public void setSede(int sede) {
		this.sede = sede;
	}
	public String getPerfil() {
		return perfil;
	}
	public void setPerfil(String perfil) {
		this.perfil = perfil;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	
}
