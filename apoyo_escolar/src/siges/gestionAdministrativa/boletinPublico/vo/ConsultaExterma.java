package siges.gestionAdministrativa.boletinPublico.vo;

public class ConsultaExterma {
	private String consultaExternaTipo;
	private long consultaExternaConsecutivo;
	private String consultaExternaRutaArchivo;
	private String consultaExternaNombreArchivo;
	private String consultaExternaExtensionArchivo;
	
	
	public long getConsultaExternaConsecutivo() {
		return consultaExternaConsecutivo;
	}
	public void setConsultaExternaConsecutivo(long consultaExternaConsecutivo) {
		this.consultaExternaConsecutivo = consultaExternaConsecutivo;
	}
	public String getConsultaExternaTipo() {
		return consultaExternaTipo;
	}
	public void setConsultaExternaTipo(String consultaExternaTipo) {
		this.consultaExternaTipo = consultaExternaTipo;
	}
	public String getConsultaExternaRutaArchivo() {
		return consultaExternaRutaArchivo;
	}
	public void setConsultaExternaRutaArchivo(String consultaExternaRutaArchivo) {
		this.consultaExternaRutaArchivo = consultaExternaRutaArchivo;
	}
	public String getConsultaExternaNombreArchivo() {
		return consultaExternaNombreArchivo;
	}
	public void setConsultaExternaNombreArchivo(String consultaExternaNombreArchivo) {
		this.consultaExternaNombreArchivo = consultaExternaNombreArchivo;
	}
	public String getConsultaExternaExtensionArchivo() {
		return consultaExternaExtensionArchivo;
	}
	public void setConsultaExternaExtensionArchivo(
			String consultaExternaExtensionArchivo) {
		this.consultaExternaExtensionArchivo = consultaExternaExtensionArchivo;
	}
}
